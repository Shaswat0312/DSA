class Solution {
    public int countPairs(List<Integer> nums, int target) {
        

        //TC : O(nlogn)
        //SC : O(n)

        Collections.sort(nums);
        int i = 0;
        int j = nums.size() - 1;

        int count = 0;

        while(i<j){
            int sum = nums.get(i) + nums.get(j);

            if(sum>=target){
                j--;
            }else{
                count+=(j-i);
                i++;
            }

        }
        return count;
    }
}
